import type { ContentBlock, WikiCategory, InlineRun } from '../../types/wiki';

let _seq = 0;
function bid(): string { _seq += 1; return `init_${Date.now().toString(36)}_${_seq}`; }
function t(text: string, marks?: InlineRun['marks']): InlineRun { return { kind: 'text', text, ...(marks ? { marks } : {}) }; }
function h1(s: string): ContentBlock { return { id: bid(), kind: 'heading1', runs: [t(s)] }; }
function h2(s: string): ContentBlock { return { id: bid(), kind: 'heading2', runs: [t(s)] }; }
function h3(s: string): ContentBlock { return { id: bid(), kind: 'heading3', runs: [t(s)] }; }
function p(s: string): ContentBlock { return { id: bid(), kind: 'paragraph', runs: [t(s)] }; }
function div(): ContentBlock { return { id: bid(), kind: 'divider' }; }
function ul(...items: string[]): ContentBlock { return { id: bid(), kind: 'bulletList', items: items.map(s => [t(s)]) }; }
function ol(...items: string[]): ContentBlock { return { id: bid(), kind: 'orderedList', items: items.map(s => [t(s)]) }; }

export interface InitialWikiPage {
  slug: string;
  title: string;
  category: WikiCategory;
  tags: string[];
  summary: string;
  contentBlocks: ContentBlock[];
}

